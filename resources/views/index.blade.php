        <h2>¿Qué es la basura tecnológica?</h2>
        <p>La basura electrónica, a menudo conocida como E-waste es un tipo de basura 
           generada por la electrónica en el mundo industrial, un tipo de basura que se genera 
           cuando un dispositivo ya sea teléfonos, tables, computadoras, impresoras y entre 
           una gran variedad de estos llegan al fin de su vida útil dejando rastros de 
           contaminación, normalmente contiene una serie de componentes peligrosos que 
           tienen un gran impacto al medio ambiente afectando incluso ala salud humana ya 
           que al no tener un lugar para su reciclaje o desecho terminan contaminado. </p>
           <br>
           <h2>Causas de generación de la Basura Tecnológica
            
           </h2>
           <p>General mente la basura tecnológica se genera a partir del fin de la vida útil de un 
           dispositivo electrónico donde este no es depositado en un sitio adecuado, donado 
           o vendido para su reparación, esta llega a zonas como, ríos, basureros o quedar 
           almacenados en lugares no adecuados. Mucha de esta basura contiene sustancias 
           tóxicas, metales pesados, componentes electrónicos, electromagnéticos y muy 
           contaminantes como pueden ser las baterías, microondas, impresoras, teclados y 
           adaptadores de corriente.</p>
           <br>
           <h2>Tipos de Basura tecnológica </h2>
           <p>Equipos de intercambio de temperatura (17%): Refrigeradores, aires 
            acondicionados, bombas de calor. </p>
            <P>Pantallas y monitores (8%): Televisores, monitores, laptops, tablets. </P>
            <p>Equipos grandes (22%): Lavadoras, secadoras, paneles solares. </p>
            <p>Lámparas (3%): LED, fluorescentes, lámparas de descarga (Forti et al., 2023).</p>

           <br>
           <h2>Impactos Ambientales</h2>
           <p>la basura tecnológica contiene metales pesados 
           (plomo y mercurio principal mente), sustancias toxicas lo que sumado al manejo 
            inadecuado contamina suelos y aguas subterráneas, si estos se queman contamina 
           el aire y  tienen una significante contribución a la huella de carbono global y 
           degradación de ecosistemas </p>
           <h2>Según el Global E-waste Monitor 2024:</h2>
           <p>Generación global: 62 millones de toneladas en 2022, proyectándose 82 Mt para 
           2030 </p>
           <p>Crecimiento anual: 2.6 Mt por año (5% anual)</p>
           <p>Tasa de reciclaje: Solo 22.3% fue recolectado y reciclado formalmente </p>
           <p>Valor perdido: $62 mil millones en materiales recuperables no reciclados</p>
           <p>Líderes regionales: Asia (34.2 Mt), Europa (13.8 Mt), América (12.4 Mt) </p>
           <p>Per cápita: Noruega lidera con 26 kg/habitante, seguida por Islandia (24.5 kg) 
           (Forti et al., 2024). </p>
           <h2>Regulaciones Actuales</h2>
           <P>Global: La Convención de Basilea actualizó sus anexos en 2022 para incluir 
            definiciones más específicas de e-waste peligroso y controlar mejor su comercio 
            transfronterizo.</P>
           <P>Unión Europea: La Directiva WEEE revisada en 2023 establece objetivos de 
            recolección del 55% para 2025 y nuevos requisitos de trazabilidad digital. </P>
           <P>Estados Unidos: 32 estados tienen ahora legislación de e-waste, con California 
            liderando la implementación de "derecho a reparar" para dispositivos electrónicos. </P>
           <P>Mercados emergentes: India implementó reglas extendidas de responsabilidad del 
            productor en 2023, mientras que Brasil lanzó su Sistema Nacional de Gestión de 
            Residuos Electrónicos (Xavier et al., 2023).</P>

           
