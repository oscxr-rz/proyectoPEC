                    el documento normativo establecido por la Nueva Escuela Mexicana (NEM), el cual marca las bases para
                    integrar esta estrategia educativa en el nivel medio superior.
                </p>
                <p class="mt-3">
                    A lo largo del proceso, se cumplieron las distintas fases que lo componen: el diagnóstico
                    participativo, donde se identificaron problemáticas reales del entorno; la planeación, donde se
                    diseñaron acciones concretas a partir de las necesidades detectadas; y la implementación del PEC,
                    resultado del trabajo colaborativo entre docentes, estudiantes y comunidad.
                </p>
                <p class="mt-3">
                    Este proceso permitió desarrollar un proyecto con sentido social, alineado a los propósitos
