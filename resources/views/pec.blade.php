                <h3 class="text-2xl font-bold mb-4">Introducción</h3>
                <p>
                    El Proyecto Aula-Escuela-Comunidad (PAEC) es una estrategia educativa que busca que lo que se
                    aprende en el aula no se quede solo en los libros, sino que tenga un impacto real dentro y fuera de
                    la escuela.
                    La idea es que los estudiantes, docentes y comunidad trabajen juntos para mejorar su entorno,
                    fortaleciendo el vínculo entre el aprendizaje escolar y la vida cotidiana.
                </p>
                <p class="mt-3">
                    Dentro de este marco, surge el Proyecto Escolar Comunitario, una actividad clave del PAEC. En este
                    proyecto, los estudiantes identifican una problemática en su comunidad —puede ser algo relacionado
                    con el medio ambiente, la salud, la convivencia, la cultura o cualquier necesidad local— y diseñan
                    una propuesta para atenderla, aplicando conocimientos adquiridos en clase y desarrollando
                    habilidades como el trabajo en equipo, la comunicación y el pensamiento crítico.
                </p>
                <p class="mt-3">
                    Este tipo de proyectos son fundamentales para la formación integral de los jóvenes, porque no solo
                    fortalecen su aprendizaje académico, sino que también los hacen más conscientes, responsables y
                    participativos.
                    Al involucrarse activamente en su comunidad, los estudiantes se convierten en agentes de cambio y
                    contribuyen al desarrollo social y comunitario.
                </p>
            </div>

