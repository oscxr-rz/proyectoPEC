                <p>
                    El PEC del CBTis No. 150 aborda de manera transversal las progresiones de aprendizaje del currículum
                    fundamental y del currículum ampliado, y se incorporan las competencias y actividades clave del
                    currículum laboral de las distintas especialidades que oferta la institución.
