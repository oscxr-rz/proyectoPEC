        <p>Remsa empresa mexicana socialmente sustentable lider en equipos electronicos. </p>
         <p>Ademas de algunas leyes creadas con el fin de reducir la basura electronica:  </p>
         <p>En México, la Ley General para la Prevención y Gestión Integral de los Residuos (LGPGIR), 
            creada en 2003, regula cómo deben manejarse los residuos, incluyendo la basura electrónica. 
            Esta ley clasifica los aparatos electrónicos como
             residuos de manejo especial y obliga a tratarlos adecuadamente para evitar contaminación. </p>
        <p>La NOM-161-SEMARNAT-2011, publicada en 2011, exige
             a las empresas que fabrican o venden electrónicos presentar planes 
             para recolectarlos y reciclarlos al final de su vida útil. 
            Esto ayuda a reducir la acumulación de residuos tecnológicos. </p>
        <p>México también forma parte del Convenio de Basilea, firmado en 1989 y
             ratificado en 1991, el cual prohíbe la importación ilegal de basura electrónica de otros
              países, protegiendo 
            el medio ambiente y la salud pública. </p>
        <p>Como referencia internacional, la Directiva RAEE de la Unión Europea 
            (año 2003) obliga a los fabricantes a reciclar sus productos. Aunque no aplica en México, 
            sirve de modelo para mejorar las políticas de reciclaje electrónico. </p>
            <p>Estas leyes ayudan a controlar, reducir y reciclar la basura electrónica,
                 protegiendo tanto al medio ambiente como a las personas</p>


        
